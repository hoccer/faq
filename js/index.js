jQuery(function($) {

	/* ==========================================================================
	   Code Highlight
	   ========================================================================== */

	function highlight() {
		$('pre code').each(function(i, e) {
		    hljs.highlightBlock(e)
		});
	}
	highlight();
	
	/* ==========================================================================
	   Responsive Videos
	   ========================================================================== */

	$('.post-content').fitVids();
	$('.post-video').fitVids();
	
});
